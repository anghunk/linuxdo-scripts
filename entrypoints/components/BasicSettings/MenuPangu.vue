<template>
  <div class="item">
    <div class="tit">{{ sort }}. 是否开启中英文混排优化显示</div>
    <input type="checkbox" :checked="modelValue" @change="$emit('update:modelValue', $event.target.checked)" />
  </div>
</template>

<script>
import $ from "jquery";
import pangu from "pangu";
export default {
  props: ["modelValue", "sort"],
  emits: ["update:modelValue"],
  data() {
    return {
      panguIntervalId: null // 添加变量存储定时器 ID
    };
  },
  created() {
    if (this.modelValue) {
      this.panguIntervalId = setInterval(() => {
        pangu.spacingElementByClassName("cooked");
        pangu.spacingElementByTagName("h1");
        document.addEventListener("DOMContentLoaded", () => {
          pangu.autoSpacingPage();
        });

        if ($(".pangutext").length < 1) {
          $("#reply-control .discard").prepend(`
          <button class="btn btn-icon-text pangutext" type="button">
            <span class="d-button-label">混排优化</span>
          </button>`);
          $(".pangutext").click(function () {
            const $textarea = $(".d-editor-input");
            let text = pangu.spacing($textarea.val());
            $textarea.focus();
            $textarea.val("");
            for (let i = 0; i < text.length; i++) {
              let char = text[i];
              $textarea.val($textarea.val() + char);
              let inputEvent = new Event("input", {
                bubbles: true,
                cancelable: true,
              });
              $textarea[0].dispatchEvent(inputEvent);
              let keyEvent = new KeyboardEvent("keydown", {
                key: char,
                char: char,
                keyCode: char.charCodeAt(0),
                which: char.charCodeAt(0),
                bubbles: true,
              });
              $textarea[0].dispatchEvent(keyEvent);
            }
          });
        }
      }, 1000);
    }
  },
  beforeUnmount() {
    // 清除定时器
    if (this.panguIntervalId) {
      clearInterval(this.panguIntervalId);
    }
  },
  // Vue 2 兼容性
  beforeDestroy() {
    // 清除定时器
    if (this.panguIntervalId) {
      clearInterval(this.panguIntervalId);
    }
  }
};
</script>
